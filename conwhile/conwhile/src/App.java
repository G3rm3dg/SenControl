public class App {
    public static void main(String[] args){
        System.out.println ("condicional while, ejercicio 2, si el numero es menor a 3 se imprime");
        int numero = 0;

        while (numero < 3) {
            System.out.println ("el numero es: " + numero);
            numero = numero + 1;
        }

        System.out.println ("fin de la ejecucion");

    }
}
